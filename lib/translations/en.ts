/**
 * Recursively widen an object type so that every string literal becomes `string`
 * and every readonly tuple/array becomes a mutable array of the widened element type.
 */
type DeepMutable<T> = T extends string
  ? string
  : T extends readonly (infer U)[]
    ? DeepMutable<U>[]
    : T extends object
      ? { [K in keyof T]: DeepMutable<T[K]> }
      : T;

